package ders12_forLoops;

public class C01_forLoop {

    public static void main(String[] args) {

        // 1’den 100’e kadar sayilari ayni satirda aralarinda bir bosluk birakarak
        //yazdirin.

        for (int i = 1; i<=100; i++) {
            System.out.print(i + " ");
        }
    }
}
